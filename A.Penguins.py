def print_ping(n):
    for i in range(n):
        print('   _~_   ', end='')
    print()
    for i in range(n):
        print('  (o o)  ', end='')
    print()
    for i in range(n):
        print(' /  V  \ ', end='')
    print()
    for i in range(n):
        print('/(  _  )\\', end='')
    print()
    for i in range(n):
        print('  ^^ ^^  ', end='')
    print()


n = int(input())
print_ping(n)
